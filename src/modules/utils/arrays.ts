// Small helpers for typed-array equality checks
export function equalU32(a: Uint32Array, b: Uint32Array) {
  if (a === b) return true
  if (a.length !== b.length) return false
  for (let i = 0; i < a.length; i++) if (a[i] !== b[i]) return false
  return true
}

export function equalU8(a: Uint8Array, b: Uint8Array) {
  if (a === b) return true
  if (a.length !== b.length) return false
  for (let i = 0; i < a.length; i++) if (a[i] !== b[i]) return false
  return true
}
